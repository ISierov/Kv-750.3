a = int(input("Please enter a whole number (A): "))
b = int(input("Please enter another whole number (B): "))

sum = a + b
difference = a - b
product = a * b
quotient = a / b
exponent = a ** b
floor = a // b
remainder = a % b


print("A + B =", sum)
print("A - B =", difference)
print("A * B =", product)
print("A / B =", quotient)
print("A ** B =", exponent)
print("A // B =", floor)
print("A % B =", remainder)
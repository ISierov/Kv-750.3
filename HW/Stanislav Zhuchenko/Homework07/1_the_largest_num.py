def the_largest_number(a: float, b: float):
    """
    The function return the largest number of two numbers: a or b
    :param a: number, type - integer or float
    :param b: number, type - integer or float
    :return: the largest number from a and b, type - integer or float
    """
    return max(a, b)


print(the_largest_number(3.1, 2))

# Creating a list with integers
int_list = [3, 45, 0, 99, 66, -9, 45, 32, -101]
# Creating an empty list for float numbers
float_list = []
# A loop that converts integer to float and appends to the list with float numbers
for i in int_list:
    float_list.append(float(i))
# Print to check the results
print(float_list)